public class ExemploBreakContinue 
{
	public static void main(String[] args) 
	{
	
		for(int numero = 1; numero <=5; numero ++) // <= 5
		{
			if(numero==3)
				break;								// VAI PARAR NO 3
		
			System.out.println(numero);
		
		}

    }
}


